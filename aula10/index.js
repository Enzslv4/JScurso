// para somar 1 a ele:

let num1 = 2
num1++
console.log(num1)

let num2 = 2
console.log(num2++) // isso faz com a conta seja feita, mas o numero n aparece
console.log(++num2) // faz a conta antes e mostra o resultado
console.log(--num2) // subtrai 1 do numero
num2 += 2 // toda vez que chamado, vai adicionar 2 ao numero
console.log(num2)